  let created = 0;
  let updated = 0;
  let errors = 0;

  for (const school of TOP_100_SCHOOLS) {
    try {
      const existing = await prisma.school.findFirst({
        where: { name: school.name },
      });

      const schoolData = {
        name: school.name,
        nameZh: school.nameZh,
        country: 'US',
        state: school.state,
        usNewsRank: school.rank,
        metadata: {
          deadlines: school.deadlines,
          applicationType: school.type,
          essayCount: school.essays,
          applicationCycle: '2025-2026',
          dataUpdated: new Date().toISOString().split('T')[0],
        },
      };

      if (existing) {
        // æ›´æ–°ç°æœ‰å­¦æ ¡
        const existingMeta =
          (existing.metadata as Record<string, unknown>) || {};
        await prisma.school.update({
          where: { id: existing.id },
          data: {
            nameZh: school.nameZh,
            usNewsRank: school.rank,
            metadata: {
              ...existingMeta,
              ...schoolData.metadata,
            },
          },
        });
        updated++;
      } else {
        // åˆ›å»ºæ–°å­¦æ ¡
        await prisma.school.create({ data: schoolData });
        created++;
      }

      console.log(`âœ… #${school.rank} ${school.nameZh} (${school.name})`);
    } catch (err: any) {
      console.log(`âŒ ${school.name}: ${err.message}`);
      errors++;
    }
  }

  console.log('\n' + '='.repeat(60));
  console.log(`ğŸ“Š ç»Ÿè®¡:`);
  console.log(`   æ–°å»º: ${created}`);
